                    <h2 style="text-align:center;">2. Add Your Contact Information and Personal Details</h2>
                    <p>A career diplomat knows what information to disclose and which is better to hold back. Likewise, there are basic
                         items that you must include in the contact information section, but you should also know what not to put on a
                          resume:</p>
                         <h3>Contact Information to Include</h3>
                         <ul><li><b>Name:</b> First name, last name.</li>
                         <li><b>Job title:</b> The one you currently have or the most recent one.</li>
                         <li><b>Phone number:</b> Personal cell phone rather than the home phone number.</li>
                         <li><b>Email address:</b> Today's preferred means of communication.</li>
                         <li><b>LinkedIn URL:</b> Include your LinkedIn profile (hiring managers will look you up anyway).</li></ul></p>
                    <p><h3>Contact Information to Leave Out</h3>
                         <ul><li><b>Date of birth:</b> Adding your birthdate could lead to ageism. Add only if required, such as for 
                         jobs serving alcohol, for example.</li>
                         <li><b>Second email or phone number:</b> A second email address, mailing address, or phone number will just 
                            confuse them (and you).</li>
                         <li><b>Photo/headshot:</b> In the United States, photos are acceptable only for jobs where appearance is part of it,
                             like acting or modeling.</li></ul>
                        <h3>Optional Contact Information</h3>
                        <ul><li><b>Home address:</b> Leave your address off your resume unless it’s required.</li>
                            <li><b>Resume title:</b> Your summary or objective would cover that part.</li>
                            <li><b>Personal website:</b> They’re becoming increasingly popular for creatives or jobs in information
                             technology. </li></ul></p>

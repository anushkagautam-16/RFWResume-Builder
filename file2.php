                    <h2 style="text-align:center;">1. Choose the Right Resume Format</h2>
                    <p>First, you need to take care of the canvas before painting your application. It’ll help you organize your resume;
                         recruiters will bid in interviews when they see your craftsmanship. <br>
                           There are three resume formats:</p>
                        <div>
                            <img src="Images/btn1img1.jpg" width=780px height=700px style="margin-left:50px;">
                        </div>
                    <p>A chronological resume has a classic resume layout, emphasizing your work experience and professional 
                        accomplishments. It lists your work history in reverse order, from the most current going backward. It’s 
                        the most popular among all-level candidates and recruiters, and Applicant Tracking Systems (ATS) can read it,
                         too. </p>
                    <p>A functional resume puts greater emphasis on your skills and qualities, hence the secondary name, skills-based
                         resume. The most important resume section is the skills summary, whereas your work history takes a back seat.
                          Use it for your creative resume or when transitioning to civilian with a military resume.<br>
                          Lastly, a combination resume that’s a hybrid of the two. It highlights your experience and skill set.
                           It’s best for professionals with plenty of relevant experience to show off. But, it’s also a good choice 
                           for career change resumes and covering employment gaps if you wish to do so.<br>
                        Is there any safe bet? Yes, there is—the chronological resume. (This guide is based on that particular format.)
                         See what it looks like:</p>
                         <div>
                            <img src="Images/btn1img2.jpg" width=780px height=700px style="margin-left:50px;">
                         </div>
                    <p>Here are the formatting rules for creating a chronological resume template:<br>
                         <b>Technicalities:</b><br>
                          <ul><li>Create even margins on your resume. 1-inch on all sides.</li>
                          <li>Set a legible resume font for the text, like Arial, Calibri, or Verdana, in 10–12 pts.
                           For headings, use 13–14 pts in font size.</li>
                          <li>Use single or 1.15 line spacing. And add an extra line before and after a heading.</li>
                          <li>Exceed a resume’s page limit only when unavoidable. Stick to one page for the best results.</li></ul></p>
                    <p><b>Sectioning:</b><br>
                        <ul><li>Start with a resume header. Make it distinctive to highlight your name and contact information.</li>
                        <li>Organize your resume sections in the following order: summary/objective, work experience, education, skills, and extras.</li>
                        <li>Use bullet points for your entries under each section.</li>
                        <li>Find resume icons for each section or skip them altogether.</li></ul></p>
                    <p><b>File format:</b><br>
                      <ul><li>See what the job ad says. Look for a preferred file formatting (PDF or .docx) in the job description. </li>
                          <li>If the company asks to send resumes in Microsoft Word .docx format, creating your resume in Word and 
                            sending it as required is a good idea.</li>
                          <li>Format your resume in PDF if there’s no requirement in the ad for your content to stay intact.
                             PDFs preserve the structure and formatting on all devices.</li>
                          <li>Name your resume file by including the job title first, then the word resume, and lastly, your full name,
                             separated by hyphens or underscores—for example, accountant-resume-jane-doe.pdf.</li></ul></p>
